    /// Currently-applied theme. Setting persists + applies the icon
    /// alternate. HUD views that read `HUDTheme.accent` /
    /// `HUDTheme.mapBackground` re-render automatically through
    /// Swift Observation tracking.
    var activeTheme: Theme {
        didSet {
            guard oldValue != activeTheme else { return }
            UserDefaults.standard.set(activeTheme.rawValue, forKey: Self.themeKey)
            generation &+= 1
            applyAlternateAppIcon(activeTheme)
        }
    }

    /// Calls `UIApplication.setAlternateIconName(_:)` for the chosen
    /// theme. iOS no-ops if the requested name is already current and
    /// silently rejects a swap if the alternate isn't bundled — both
    /// fine, the completion error is logged and swallowed in DEBUG.
    private func applyAlternateAppIcon(_ theme: Theme) {
        let target = theme.alternateIconName
        let app = UIApplication.shared
        guard app.supportsAlternateIcons else { return }
        if app.alternateIconName == target { return }
        app.setAlternateIconName(target) { error in
            if let error {
                #if DEBUG
                print("ThemeManager: setAlternateIconName(\(target ?? "nil")) failed: \(error)")
                #endif
            }
        }
    }

