        // culled ones (avalanche, alpenglow, verdant, …) fall through
        // to Original.
        case "glacier": migrated = .glacier
        default:        migrated = .original
        }
        self.activeTheme = migrated
        UserDefaults.standard.set(migrated.rawValue, forKey: Self.themeKey)
        UserDefaults.standard.removeObject(forKey: Self.legacyAccentKey)
        UserDefaults.standard.removeObject(forKey: Self.legacyIconKey)
    }

    /// Reset theme to the launch default. Called on account deletion
    /// so a fresh account that signs in next on this device starts
    /// clean. Sign-out alone does NOT reset.
    func resetToDefault() {
        activeTheme = .original
    }
}
