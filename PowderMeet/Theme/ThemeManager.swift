//
//  ThemeManager.swift
//  PowderMeet
//
//  A Theme bundles three things into a single named pick:
//
//    • icon  — the home-screen AppIcon variant
//      (`UIApplication.setAlternateIconName(_:)`)
//    • text  — the HUD accent color used for labels, chrome, selected
//      states (`HUDTheme.accent` reads this through Swift Observation
//      tracking, so changes ripple process-wide without manual
//      invalidation)
//    • bg    — the app's root background tint, near-black with a
//      whisper of hue so the theme's identity reads on every sheet
//      and root surface (`HUDTheme.mapBackground`)
//
//  The launch ORIGINAL theme keeps the legacy black background; every
//  other theme tints the bg toward its accent (~5–10% luminance) so
//  the chrome reads as belonging to that theme without ever stealing
//  contrast from the content on top.
//
//  Source of truth for the metadata below lives in
//  `tools/generate_theme_icons.py`. That script also generates the
//  icon PNGs and emits the Info.plist `CFBundleAlternateIcons` block.
//  Edit the script first, paste its output into this file second.
//

